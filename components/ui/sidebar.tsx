const sidebarMenuButtonVariants = cva(
  "group flex w-full items-center gap-2 rounded-md px-3 py-2 text-sm font-medium transition-colors hover:bg-accent hover:text-accent-foreground focus-visible:outline-none focus-visible:ring-1 focus-visible:ring-ring disabled:pointer-events-none disabled:opacity-50",
  {
    variants: {
      isActive: {
        true: "bg-accent text-accent-foreground",
        false: "transparent",
      },
    },
    defaultVariants: {
      isActive: false,
    },
  },
)

export interface SidebarMenuButtonProps
  extends React.ButtonHTMLAttributes<HTMLButtonElement>,
    VariantProps<typeof sidebarMenuButtonVariants> {
  asChild?: boolean
  tooltip?: string
  isActive?: boolean
}

// Sidebar menu button
export const SidebarMenuButton = React.forwardRef<HTMLButtonElement, SidebarMenuButtonProps>(
  ({ className, isActive, tooltip, asChild = false, ...props }, ref) => {
    const Comp = asChild ? React.Fragment : "button"
    const childProps = asChild ? props.children.props : {}

    return (
      <Comp
        ref={ref}
        className={cn(sidebarMenuButtonVariants({ isActive }), className)}
        title={tooltip}
        {...(asChild ? {} : props)}
      >
        {asChild ? React.cloneElement(props.children, { ...childProps }) : props.children}
      </Comp>
    )
  },
)
SidebarMenuButton.displayName = "SidebarMenuButton"

// Sidebar trigger button
export function SidebarTrigger() {
  const { open, setOpen } = useSidebar()
  const isMobile = useMobile()

  if (!isMobile) return null

  return (
    <Button
      variant="ghost"
      size="icon"
      className="h-9 w-9"
      onClick={() => setOpen(!open)}
      aria-label={open ? "Close sidebar" : "Open sidebar"}
    >
      {open ? <X className="h-5 w-5" /> : <MenuIcon className="h-5 w-5" />}
    </Button>
  )
}

